function App() {
	const [options, setOptions] = useState(data);
	const [term, setTerm] = useState("");
	const [articlesData, setArticlesData] = useState("");
	const [proposalVisible, setProposalVisible] = useState(false);

	const handleSearch = (searchValue) => {
		setTerm(searchValue);
	};

	const handleSetTerm = (term) => {
		if (term.length === 0) {
			setArticlesData([]);
			return;
		}

		const filteredData = options.filter(
			({ model }) => model.toLowerCase().indexOf(term.toLowerCase()) > -1
		);

		setArticlesData(filteredData);
	};

	const handleOpenOptions = () => {
		if (term.length === 0) {
			setArticlesData([]);

			return;
		}

		setProposalVisible(true);
	};

	const handleCloseOptions = () => {
		setProposalVisible(false);
	};

	useEffect(() => {
		const handleClick = (event) => {
			if (
				!event.target.classList.contains("list__button") ||
				!event.target.classList.contains("search__controls")
			) {
				handleCloseOptions();
			}
		};

		document.addEventListener("click", handleClick, true);

		return () => {
			document.removeEventListener("click", handleClick, true);
		};
	}, []);

	return (
		<div className="App">
			<section className="section">
				<div className="shell">
					<header className="section__head">
						<Search
							data={options}
							handleSearch={handleSearch}
							term={term}
							handleSetTerm={handleSetTerm}
							proposalVisible={proposalVisible}
							handleOpenOptions={handleOpenOptions}
							handleCloseOptions={handleCloseOptions}
						/>
					</header>

					{articlesData.length > 0 && (
						<div className="section__body">
							<Articles data={articlesData} />
						</div>
					)}
				</div>
			</section>
		</div>
	);
}

export default App;
